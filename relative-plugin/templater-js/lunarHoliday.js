/**
 * 휴일을 체크하여 휴일일 경우 휴일 데이터 반환
 */
function getHoliday(solarDate, lunarDate) {
  var i;
  var memorial;
  var addHolidays = ["설 전날", 12, 0, 2]; //가변적인 설 전날

  if (lunarMonthTable[solarDate.year - 1 - 1799][11] == 1) addHolidays[2] = 29;
  else if (lunarMonthTable[solarDate.year - 1 - 1799][11] == 2)
    addHolidays[2] = 30;

  if (addHolidays[1] == lunarDate.month && addHolidays[2] == lunarDate.day) {
    return addHolidays;
  }
  for (i = 0; i < holidayTable.length; i++) {
    if (
      holidayTable[i][3] == 1 &&
      holidayTable[i][1] == solarDate.month &&
      holidayTable[i][2] == solarDate.day
    )
      return holidayTable[i];
    //윤달의 공휴일 처리에 대한 예외처리
    if (lunarDate.leapMonth && lunarDate.month == 4 && lunarDate.day == 8) {
      return null;
    }
    if (
      lunarDate.leapMonth &&
      lunarDate.month == 8 &&
      lunarDate.day > 13 &&
      lunarDate.day < 17
    ) {
      return null;
    }
    if (
      holidayTable[i][3] == 2 &&
      holidayTable[i][1] == lunarDate.month &&
      holidayTable[i][2] == lunarDate.day
    )
      return holidayTable[i];
  }
  return null;
}

function splitDate(year, month, day) {
  if (year instanceof Date) {
    month = year.getMonth() + 1;
    day = year.getDate();
    year = year.getFullYear();
  }
  return { year: year, month: month, day: day };
}
function getDayOfWeek(date) {
  var d = new Date(date.year, date.month - 1, date.day);
  switch (d.getDay()) {
    case 0:
      return "일";
    case 1:
      return "월";
    case 2:
      return "화";
    case 3:
      return "수";
    case 4:
      return "목";
    case 5:
      return "금";
    case 6:
      return "토";
  }
}

