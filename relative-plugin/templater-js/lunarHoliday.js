/**
 * 입력한 음력 날짜로 양력 날짜 반환
 * isLeapMonth : 입력한 음력 날짜가 윤달인가?
 */
function getSolar(year, month, day, isLeapMonth) {
  var date = splitDate(year, month, day);
  var o = calcLunar(date.year, date.month, date.day, 2, isLeapMonth ? 1 : 0);
  o.dayOfWeek = getDayOfWeek(o);
  return o;
}
