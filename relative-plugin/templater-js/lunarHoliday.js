/**
 * 입력한 양력 날짜로 음력 날짜 반환
 */
function getLunar(year, month, day) {
  var date = splitDate(year, month, day);
  var o = calcLunar(date.year, date.month, date.day, 1);
  o.dayOfWeek = getDayOfWeek(date);
  return o;
}
